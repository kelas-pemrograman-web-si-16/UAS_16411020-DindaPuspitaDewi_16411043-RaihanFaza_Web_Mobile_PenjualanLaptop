package com.example.laptopappstore.data;

public class DataPemesanan {
    String kodepemesanan, nama, notelp, kodelaptop;

    public String getKodePemesanan() {
        return kodepemesanan;
    }

    public void setKodePemesanan(String kodepemesanan) {
        this.kodepemesanan = kodepemesanan;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNotelp() {
        return notelp;
    }

    public void setNotelp (String notelp) {
        this.notelp = notelp;
    }


    public String getKodeLaptop() {
        return kodelaptop;
    }

    public void setKodeLaptop(String kodeLaptop) {
        this.kodelaptop = kodeLaptop;
    }
}

